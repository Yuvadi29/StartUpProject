class Startup {
  final int id;
  final String name;
  final String industry;
  final String subVertical;
  final String city;
  final String investors;
  final String investmentType;
  final int amount;

  Startup({
    required this.id,
    required this.name,
    required this.industry,
    required this.subVertical,
    required this.city,
    required this.investors,
    required this.investmentType,
    required this.amount,
  });
}
